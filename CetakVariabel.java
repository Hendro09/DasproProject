public class CetakVariabel {
	public static void main ( String [] args ) {

	int Number;
	Number = 10 ;
	System.out.println( "Number = " + Number );

	char Letter;
	Letter= 'a';
	System.out.println( Letter );

	boolean result;
	result = false ;
	System.out.println( result );

	String str;
	str= "hello" ;
	System.out.println(str);

	}
}